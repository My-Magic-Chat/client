#!/bin/bash

# variables
ENV_FILE=${1:-$ENV_FILE}
ENV=${2:-$ENV}
PROJECT=${3:-$PROJECT}
API_KEY=${4:-$API_KEY}
AUTH_DOMAIN=${5:-$AUTH_DOMAIN}
PROJECT_ID=${6:-$PROJECT_ID}
STORAGE_BUCKET=${7:-$STORAGE_BUCKET}
MESSAGING_SENDER_ID=${8:-$MESSAGING_SENDER_ID}
APP_ID=${9:-$APP_ID}
MEASUREMENT_ID=${10:-$MEASUREMENT_ID}

touch $ENV_FILE
echo ENV=$ENV >> $ENV_FILE
echo PROJECT=$PROJECT >> $ENV_FILE
echo REACT_APP_API_KEY=$API_KEY >> $ENV_FILE
echo REACT_APP_AUTH_DOMAIN=$AUTH_DOMAIN >> $ENV_FILE
echo REACT_APP_PROJECT_ID=$PROJECT_ID >> $ENV_FILE
echo REACT_APP_STORAGE_BUCKET=$STORAGE_BUCKET >> $ENV_FILE
echo REACT_APP_MESSAGING_SENDER_ID=$MESSAGING_SENDER_ID >> $ENV_FILE
echo REACT_APP_ID=$APP_ID >> $ENV_FILE
echo REACT_APP_MEASUREMENT_ID=$MEASUREMENT_ID >> $ENV_FILE
